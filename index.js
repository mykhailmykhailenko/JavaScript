// % - Залишок від цілочисленного поділу (показує остаток при діленні)

console.log(4%2) // якщо цисло націло ділиться на 2 без залишку, тобто  результат цього консолю буде 0 - то число парне

console.log(1%2) // якщо число націло не ділиться на 2 і буде з залишком, тобто результат консолю буде 1 - то число не парне